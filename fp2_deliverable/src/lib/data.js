// ── Color scales (Andrew's Visual Design Study palette) ───────────────────────

/**
 * Choropleth color scale for monthly housing payment.
 * Bins: [0–2000, 2000–3000, 3000–4000, 4000–5000, 5000+]
 * Uses Housing Transition palette (yellow → deep orange).
 */
export function makePaymentColorScale() {
  return d3.scaleThreshold()
    .domain([2000, 3000, 4000, 5000])
    .range(['#f4e4a8', '#f0c860', '#e09030', '#d4730a', '#9a4e00'])
}

/**
 * Color scale for corporate ownership rate (0–1).
 * Uses Investor Activity palette (light teal → deep teal).
 */
export function makeCorpOwnColorScale() {
  return d3.scaleSequential()
    .domain([0, 1])
    .interpolator(d3.interpolate('#a8e6d8', '#1a7a5e'))
}

/**
 * Color scale for eviction count.
 * Uses Pressure/Harm palette (light pink → deep red).
 */
export function makeEvictionColorScale(maxEvictions) {
  return d3.scaleSequential()
    .domain([0, maxEvictions])
    .interpolator(d3.interpolate('#f4b8b8', '#c0392b'))
}

