// ── Condo conversion data loader (kept for reference) ─────────────────────────
export async function loadCondoData() {
  const raw = await d3.csv('./data/viz2_condo_conversions_by_zip_year.csv', d => ({
    zip:                   String(d.zip).padStart(5, '0'),
    year:                  +d.post_year,
    num_conversions:       +d.num_conversions,
    total_new_condo_units: +d.total_new_condo_units,
    median_value_jump:     +d.median_value_jump,
    median_value_jump_pct: +d.median_value_jump_pct,
  }))
  return raw.filter(d => !isNaN(d.year) && !isNaN(d.num_conversions))
}
